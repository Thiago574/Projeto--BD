    private void ButExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButExcluirActionPerformed
        if(TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.isEmpty()){
            JOptionPane.showMessageDialog(null, "Nenhum produto cadastrado.", "Aviso!",
                JOptionPane.ERROR_MESSAGE);   
        }else{
            int index = jTableAlterarDadosP.getSelectedRow();
            if (index>=0){
                if(!Principal.department.isEmpty()){
                    if(TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index) instanceof Departamento){
                        Departamento dep = (Departamento) TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index);       
                        for(int i = 0;i<Principal.department.size();i++){
                            if(Principal.department.get(i).getNome().equals(dep.getNome()) & 
                                Principal.department.get(i).getUtilidade().equals(dep.getUtilidade())){
                                Principal.department.remove(i);
                            }
                        }
                    }
                    
                }
                if(!Principal.market.isEmpty()){
                    if(TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index) instanceof Mercado){
                        Mercado mark = (Mercado) TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index);       
                        for(int i = 0;i<Principal.market.size();i++){
                            if(Principal.market.get(i).getNome().equals(mark.getNome()) & 
                                Principal.market.get(i).getUtilidade().equals(mark.getUtilidade())){
                                Principal.market.remove(i);
                            }
                        }
                    }
                    
                }
                
                TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.remove(index);
                carregarTabelaProd();
                txtNomeProduto.setText("");
                txtRating.setText("");
                txtTipoProduto.setText("");
                txtCor.setText("");
                txtMarca.setText("");
                txtUtilidade.setText("");
                jFmtDataValidade.setText("");
            }
        }
    }//GEN-LAST:event_ButExcluirActionPerformed

