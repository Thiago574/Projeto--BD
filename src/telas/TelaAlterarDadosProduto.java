    public void carregarTabelaProd(){
        DefaultTableModel modelo = new DefaultTableModel(new Object [] {"Nome","Rating","Tipo"},0);       
        for(int i=0;i<TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.size();i++){
            Object linha[] = new Object[]{TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(i).getNome(), 
                    TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(i).getRating(), TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(i).getTipo()};
            modelo.addRow(linha);
            }
        jTableAlterarDadosP.setModel(modelo);
    }
    public void produtoSelecionado(){
                if(TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.isEmpty()){
            JOptionPane.showMessageDialog(null, "Nenhum produto cadastrado.", "Aviso!",
                JOptionPane.ERROR_MESSAGE);   
        }else{
            int index = jTableAlterarDadosP.getSelectedRow();
            if(index>=0){
                txtNomeProduto.setText(TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index).getNome());
                txtRating.setText(Integer.toString(TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index).getRating()));
                txtTipoProduto.setText(TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index).getTipo());
                if(TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index) instanceof Departamento){   
                    Produto objdep = TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index);
                    Departamento dep = (Departamento) objdep;
                    txtCor.setText(dep.getCor());
                    txtMarca.setText(dep.getMarca());
                    txtUtilidade.setText(dep.getUtilidade());
                    jFmtDataValidade.setEnabled(false);
                }
                if(TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index) instanceof Mercado){ 
                    jFmtDataValidade.setEnabled(true);
                    Produto objmarket = TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.get(index);
                    Mercado mer = (Mercado)objmarket;
                    txtCor.setText(mer.getCor());
                    txtMarca.setText(mer.getMarca());
                    txtUtilidade.setText(mer.getUtilidade());
                    jFmtDataValidade.setText(mer.getDatadevalidade());
                    
                }           
            }else{
                JOptionPane.showMessageDialog(null, "Selecione um Produto na Tabela ao lado.", "Aviso!",
                    JOptionPane.ERROR_MESSAGE);   
            }
        }
    }
    public TelaAlterarDadosProduto() {
        initComponents();       
        if(!TelaCadastro.users.get(TelaLogin.indexUser).produtos_user.isEmpty())
            carregarTabelaProd();
        
        readJTable();
        
    }
    
    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for (Mercado p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getIdProduto(),
                p.getNome(),
                p.getRating(),
                p.getTipo(),
                p.getCor(),
                p.getMarca(),
                p.getUtilidade(),
            });

        }

    }
    
   

