    private void ButEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButEntrarActionPerformed
        
       
        //System.out.println(user.getUsuarios().size());
        //System.out.println(user.getUsuarios().isEmpty());
        
        if(txtLogin.getText().equals("") | txtSenha.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos antes de clicar em Entrar.", "Aviso!",
                JOptionPane.ERROR_MESSAGE); 
            
            txtLogin.setText("");
            txtSenha.setText("");
                    
            txtLogin.requestFocus();
           
        } else if(txtLogin.getText().equals("dev") && txtSenha.getText().equals("1234")){
            String data = "99/99/9999";
            Usuario dev = new Usuario("Lagoinha", "190", "devi", data, "000.000.000-00", "1234", "dev", 10001);
            if(TelaCadastro.users == null){
                TelaCadastro.users = new ArrayList();
            }
            TelaCadastro.users.add(dev);
            indexUser = 0;
            this.setVisible(false);
            new TelaMenuUsuario().setVisible(true);
        
           
        }
        else if(TelaCadastro.users == null){
            JOptionPane.showMessageDialog(null, "Por favor cadastre um usuário.", "Aviso!",
                    JOptionPane.ERROR_MESSAGE);
        }else{
            if(CheckUsersLogPassword(txtLogin.getText(), txtSenha.getText())){
                this.setVisible(false);
                new TelaMenuUsuario().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "Senha ou usuário incorretos.", "Aviso!",
                    JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_ButEntrarActionPerformed

