    public boolean CheckUsersLogPassword(String login, String senha){
        for(int i = 0;i<TelaCadastro.users.size();i++){
            if(TelaCadastro.users.get(i).getLogin().equals(login) & 
                    TelaCadastro.users.get(i).getSenha().equals(senha)){
                indexUser = i;
                return true;
            }
        }
        return false;
    }

